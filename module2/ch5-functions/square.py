"""
This file contains two functions to perform calculations on squares using its side.
A function to get the perimeter of a square, and a function to get the area of a square
"""

# this function will take as a parameter a side of a square and return the area
def square_area(side):
    return side * side

# this function will take as a parameter a side of a square, and return the perimeter
def square_perimeter(side):
    return side * 4